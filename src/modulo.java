
public class modulo {
	public static void soma(double num1, double num2) {

		double total = (num1 + num2);
		
		System.out.println("As Soma " +num1 + " + " +num2 + " é " +total);

		
	}

	public static void substracao(double num1, double num2) {

		double total = (num1 - num2);

		System.out.println("A Substracao " +num1 + " - " +num2 + " é " +total);

	}

	public static void multiplcacao(double num1, double num2) {

		double total = (num1 * num2);

		System.out.println("A Multiplicação " +num1 + " x " +num2 + " é " +total);
	}

	public static void  divisao(double num1, double num2) {
		
		double total = (num1 / num2);
		
		System.out.println("A Divisao " +num1 + " / " +num2 + " é " +total);
	}

}
